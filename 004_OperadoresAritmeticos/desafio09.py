# Faça um programa que leia um número inteiro qualquer e mostre a sua tabuada.

numero = int(input('Insira um valor inteiro para saber a sua tabuada:'))

zero = numero * 0
um = numero * 1
dois = numero * 2
tres = numero * 3
quatro = numero * 4
cinco = numero * 5
seis = numero * 6
sete = numero * 7
oito = numero * 8
nove = numero * 9
dez = numero * 10

print (numero, 'x 0 = ', zero)
print (numero, 'x 1 = ', um)
print (numero, 'x 2 = ', dois)
print (numero, 'x 3 = ', tres)
print (numero, 'x 4 = ', quatro)
print (numero, 'x 5 = ', cinco)
print (numero, 'x 6 = ', seis)
print (numero, 'x 7 = ', sete)
print (numero, 'x 8 = ', oito)
print (numero, 'x 9 = ', nove)
print (numero, 'x 10 = ', dez)